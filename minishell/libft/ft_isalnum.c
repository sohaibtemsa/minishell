#include <unistd.h>
#include "libft.h"

int	ft_isalnum(const int c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	if (c >= 'a' && c <= 'z')
		return (1);
	if (c >= '0' && c <= '9')
	{
		return (1);
	}
	return (0);
}
